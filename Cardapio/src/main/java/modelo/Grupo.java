package modelo;

/**
 *
 * @author Roberto
 */
public class Grupo {
    private int id;
    private String nome;

    public Grupo(int id, String nome) {
        this.id = id;
        this.nome = nome;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
    
}
